def add_numbers(a, b):
    return a + b

def sub_numbers(a, b):
    return a - b

def multiplication(a, b):
    return a * b

def division(a, b):
    """
    This function performs division of two numbers.

    parameter:
        a (float): The dividend
        b (float): The divisor

    returns:
        float: The quotient of a and b
    """
    return a / b

